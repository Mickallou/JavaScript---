/**
 * Escape Characters (\ in a string)
 * 
 * for \ write \\
 * for new line write \r\n
 * for ' in a string \'
 * for "" in a string \"
 * for tab character write \t
 * 
 */
console.log("Hi my name is \"Mickael\" 'Allouche' 04\\10\\1998");
console.log("Dog\'s");
console.log("Hello\tMy name is");
console.log("Hello\r\nYOU");

